// Animation speeds per mood (ms)
const SPEEDS: Record<MascotMood, number> = {
  idle: 600,
  thinking: 150,
  asking: 500,
  answering: 350,
  executing: 300,
  done: 400,
  error: 100,
};

const LABELS: Record<MascotMood, string> = {
  idle: "",
  thinking: "thinking...",
  asking: "your turn",
  answering: "got it",
  executing: "working...",
  done: "all done",
  error: "uh oh",
};

export function Mascot({ mood }: { mood: MascotMood }) {
  const [frame, setFrame] = useState(0);
  const frames = FRAMES[mood];
  const speed = SPEEDS[mood];
  const label = LABELS[mood];

  useEffect(() => {
    setFrame(0);
    const interval = setInterval(() => {
      setFrame((f) => (f + 1) % frames.length);
    }, speed);
    return () => clearInterval(interval);
  }, [mood, frames.length, speed]);

  const currentFrame = frames[frame % frames.length];

  return (
    <Box flexDirection="column">
      {currentFrame.map((line, i) => (
        <Text key={i} color="cyan">
          {line[0]}
        </Text>
      ))}
      {label ? (
        <Text color="gray" dimColor>
          {"            "}{label}
        </Text>
      ) : null}
    </Box>
  );
}

// Inline mini face for headers
const MINI_FRAMES: Record<MascotMood, string[]> = {
  idle: ["[¬∑ ¬∑]", "[- -]"],
  thinking: ["[‚ó† ‚ó†]", "[‚ó° ‚ó°]"],
  asking: ["[? ?]", "[  ?]"],
  answering: ["[‚óÜ ‚óÜ]", "[‚óá ‚óá]"],
  executing: ["[‚Ė™ ‚Ė™]", "[¬∑ ¬∑]"],
  done: ["[‚óÜ ‚óÜ]", "[‚óá ‚óá]"],
  error: ["[‚ĖĎ ‚ĖĎ]", "[‚Ėď ‚Ėď]"],
};

export function MiniMascot({ mood }: { mood: MascotMood }) {
  const [frame, setFrame] = useState(0);
  const frames = MINI_FRAMES[mood];
  const speed = SPEEDS[mood];

  useEffect(() => {
    setFrame(0);
    const interval = setInterval(() => {
      setFrame((f) => (f + 1) % frames.length);
    }, speed);
    return () => clearInterval(interval);
  }, [mood, frames.length, speed]);

  return <Text color="cyan">{frames[frame % frames.length]}</Text>;
}

export function statusToMood(
  status: string,
  _phase?: string
): MascotMood {
  switch (status) {
    case "thinking":
      return "thinking";
    case "asking":
      return "asking";
    case "executing":
      return "executing";
    case "done":
      return "done";
    case "error":
      return "error";
    default:
      return "idle";
  }
}
